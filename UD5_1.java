public class UD5_1 {

	public static void main(String[] args) {
		int x = 15, y = 10;
		
		System.out.println("Variable 1 = " + x + ", variable 2 = " + y);
		if (x > y) {
			System.out.println("La primera variable es mayor que la segunda");
		}
		else if (x == y) {
			System.out.println("Las dos variables son iguales");
		}
		else {
			System.out.println("La segunda variable es mayor que la primera");
		}
	}
}